/*LIST OF PRODUCTS PURCHASED BY A SPECIFIC CUSTOMER */

SELECT CONCAT(C.FIRSTNAME,' ',C.LASTNAME) AS CNAME,
P.PRODUCTNAME,OD.QUANTITY FROM CUSTOMER AS C
INNER JOIN ORDERS AS O
ON C.CUSTOMERID=O.CUSTOMERID
INNER JOIN ORDERDETAILS AS OD
ON O.ORDERID=OD.ORDERID
INNER JOIN PRODUCT AS P
ON OD.PRODUCTID=P.PRODUCTID
WHERE C.FIRSTNAME='ALICE' AND C.LASTNAME='SMITH';

/* FIND CUSTOMER WHO HAVE ORDERED A LAPTOP */

SELECT CONCAT(C.FIRSTNAME,' ',C.LASTNAME)AS CNAME,P.PRODUCTNAME
FROM CUSTOMER AS C
INNER JOIN ORDERS AS O
ON C.CUSTOMERID=O.CUSTOMERID
INNER JOIN ORDERDETAILS AS OD
ON O.ORDERID=OD.ORDERID
INNER JOIN PRODUCT AS P
ON OD.PRODUCTID=P.PRODUCTID
WHERE P.PRODUCTNAME='LAPTOP';



